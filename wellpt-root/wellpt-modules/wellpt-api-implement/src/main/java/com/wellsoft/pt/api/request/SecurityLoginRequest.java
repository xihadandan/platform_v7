package com.wellsoft.pt.api.request;

import com.wellsoft.pt.api.WellptRequest;
import com.wellsoft.pt.api.internal.suport.ApiServiceName;
import com.wellsoft.pt.api.response.SecurityLoginResponse;

public class SecurityLoginRequest extends WellptRequest<SecurityLoginResponse> {

    /**
     * @Fields deviceId : 客户端设备id
     */
    private String deviceId;
    private String username;
    private String password;
    private String source;

    /**
     * 来自微信企业号的访问
     */
    private String accessToken;
    private int devicetype = 1;
    private String token;


    public int getDevicetype() {
        return devicetype;
    }

    public void setDevicetype(int devicetype) {
        this.devicetype = devicetype;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getAccessToken() {
        return accessToken;
    }

    public void setAccessToken(String accessToken) {
        this.accessToken = accessToken;
    }

    @Override
    public String getApiServiceName() {
        return ApiServiceName.SECURITY_LOGIN;
    }

    @Override
    public Class<SecurityLoginResponse> getResponseClass() {
        return SecurityLoginResponse.class;
    }

    /**
     * @return the deviceId
     */
    public String getDeviceId() {
        return deviceId;
    }

    /**
     * @param deviceId the deviceId to set
     */
    public void setDeviceId(String deviceId) {
        this.deviceId = deviceId;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }
}
