-- 字典分类
CREATE TABLE CD_DATA_DICTIONARY_CATEGORY
(
    "UUID"        NUMBER(20, 0) NOT NULL,
    "CREATOR"     VARCHAR2(20 CHAR),
    "CREATE_TIME" TIMESTAMP(6),
    "MODIFIER"    VARCHAR2(20 CHAR),
    "MODIFY_TIME" TIMESTAMP(6),
    "REC_VER"     NUMBER(10, 0),
    "SYSTEM"      VARCHAR2(64),
    "TENANT"      VARCHAR2(64),
    "NAME"        VARCHAR2(64),
    "SORT_ORDER"  NUMBER(10, 0),
    "TYPE"        NUMBER(10, 0),
    "MODULE_ID"   VARCHAR2(64),
    "REMARK"      VARCHAR2(200),
    PRIMARY KEY ("UUID")
);
COMMENT
    ON TABLE CD_DATA_DICTIONARY_CATEGORY IS '字典分类';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY_CATEGORY.UUID IS 'UUID，系统字段';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY_CATEGORY.CREATOR IS '创建人';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY_CATEGORY.CREATE_TIME IS '创建时间';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY_CATEGORY.MODIFIER IS '修改人';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY_CATEGORY.MODIFY_TIME IS '修改时间';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY_CATEGORY.SYSTEM IS '归属系统';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY_CATEGORY.TENANT IS '归属租户';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY_CATEGORY.NAME IS '分类名称';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY_CATEGORY.SORT_ORDER IS '排序号';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY_CATEGORY.TYPE IS '类型，0平台字典分类，1业务字典分类';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY_CATEGORY.MODULE_ID IS '模块ID';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY_CATEGORY.REMARK IS '备注';


-- 数据字典
