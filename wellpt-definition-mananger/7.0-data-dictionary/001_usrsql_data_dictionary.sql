    ON COLUMN CD_DATA_DICTIONARY.CREATE_TIME IS '创建时间';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY.MODIFIER IS '修改人';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY.MODIFY_TIME IS '修改时间';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY.SYSTEM IS '归属系统';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY.TENANT IS '归属租户';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY.NAME IS '字典名称';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY.CODE IS '字典编码';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY.EXT_DEFINITION_JSON IS '扩展定义JSON信息';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY.MODULE_ID IS '模块ID';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY.CATEGORY_UUID IS '字典分类UUID';
COMMENT
    ON COLUMN CD_DATA_DICTIONARY.REMARK IS '备注';


