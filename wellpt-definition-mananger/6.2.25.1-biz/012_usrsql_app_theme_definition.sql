    ON COLUMN APP_THEME_DEFINITION.CREATE_TIME IS '创建时间';
COMMENT
    ON COLUMN APP_THEME_DEFINITION.MODIFIER IS '修改人';
COMMENT
    ON COLUMN APP_THEME_DEFINITION.MODIFY_TIME IS '修改时间';
COMMENT
    ON COLUMN APP_THEME_DEFINITION.REC_VER IS '版本号';
COMMENT
    ON COLUMN APP_THEME_DEFINITION.SYSTEM_UNIT_ID IS '系统单位ID';
COMMENT
    ON COLUMN APP_THEME_DEFINITION.NAME IS '名称';
COMMENT
    ON COLUMN APP_THEME_DEFINITION.ID IS 'ID';
COMMENT
    ON COLUMN APP_THEME_DEFINITION.CODE IS '编号';
COMMENT
    ON COLUMN APP_THEME_DEFINITION.SORT_ORDER IS '排序号';
COMMENT
    ON COLUMN APP_THEME_DEFINITION.APPLY_TO IS '作用范围';
COMMENT
    ON COLUMN APP_THEME_DEFINITION.ENABLED IS '是否启用';
COMMENT
    ON COLUMN APP_THEME_DEFINITION.DEFINITION_JSON_UUID IS '主题定义JSON UUID';
COMMENT
    ON COLUMN APP_THEME_DEFINITION.REMARK IS '备注';


