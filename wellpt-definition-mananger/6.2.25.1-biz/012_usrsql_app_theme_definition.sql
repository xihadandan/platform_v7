CREATE TABLE APP_THEME_DEFINITION
(
    "UUID"                 VARCHAR2(50 CHAR) NOT NULL ENABLE,
    "CREATOR"              VARCHAR2(20 CHAR),
    "CREATE_TIME"          TIMESTAMP(6),
    "MODIFIER"             VARCHAR2(20 CHAR),
    "MODIFY_TIME"          TIMESTAMP(6),
    "REC_VER"              NUMBER(10, 0),
    "SYSTEM_UNIT_ID"       VARCHAR2(12 CHAR),
    "NAME"                 VARCHAR2(100 CHAR),
    "ID"                   VARCHAR2(50 CHAR),
    "CODE"                 VARCHAR2(20 CHAR),
    "SORT_ORDER"           NUMBER(10, 0),
    "APPLY_TO"             VARCHAR2(50 CHAR),
    "ENABLED"              NUMBER(1),
    "DEFINITION_JSON_UUID" VARCHAR2(50 CHAR),
    "REMARK"               VARCHAR2(200 CHAR),
    PRIMARY KEY ("UUID")
);
COMMENT
    ON TABLE APP_THEME_DEFINITION IS '应用主题定义';
COMMENT
    ON COLUMN APP_THEME_DEFINITION.UUID IS 'UUID，系统字段';
COMMENT
